public class Horse {
    private String name;
    private String color;
    private Integer anioNa;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public Integer getAnioNa() {
        return anioNa;
    }

    public void setAnioNa(Integer anioNa) {
        this.anioNa = anioNa;
    }
    
    
}
